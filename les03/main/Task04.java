package by.htp.les03.main;

//  Составить программу: равны ли два числа а и b?

public class Task04 {
	public static void main(String[] args) {

		int a = 2;
		int b = 4;

		if (a > b) {
			System.out.println("a != b");
		} else if (a < b) {
			System.out.println("a != b");
		} else if (a != b) {
			System.out.println("a != b");
		} else {
			System.out.println("a = b");
		}
	}

}
